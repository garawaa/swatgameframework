package org.swat.data;

/**
 * The enumeration detailing the type of moves that can be made in a game
 * 
 * move - the pieces can be moved only
 * add - new pieces can be added only
 * move_add - both of the above types of moves can be made
 * 
 * @author steve
 *
 */
public enum GAME_TYPE {
	
	MOVE, ADD, MOVE_ADD;

}
